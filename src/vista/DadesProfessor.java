    private void nifTxtFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_nifTxtFocusLost
        jLabel1.setText("");
        if (professorJDBC.existeProfessor(nifTxt.getText())) {
            JOptionPane.showMessageDialog(this, "Ya existeix un professor amb aquest nif", "ERROR: NIF duplicat", JOptionPane.ERROR_MESSAGE);
            nifTxt.requestFocusInWindow();
            nifTxt.selectAll();
            aceptarBtn.setEnabled(false);
        } else if (!nifTxt.getText().equals("")) {
            aceptarBtn.setEnabled(true);
        } else {
            aceptarBtn.setEnabled(false);
        }
    }//GEN-LAST:event_nifTxtFocusLost

    private void nifTxtKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_nifTxtKeyTyped
        jLabel1.setText(null);
        char c = evt.getKeyChar();
        if (c != VK_BACK_SPACE && c != KeyEvent.VK_DELETE && c != KeyEvent.VK_ENTER) {
            if (nifTxt.getText().length() > 8) {
                evt.consume();
                jLabel1.setText("maxim 9 caracters");
            } else if (nifTxt.getText().length() > 7) {
                String lletraFinal=Character.toString(c).toUpperCase();
                c=lletraFinal.charAt(0);
                evt.consume();
                nifTxt.setText(nifTxt.getText().substring(0,8)+c);
                if (c < 'A' || c > 'Z') {
                    evt.consume();
                    jLabel1.setText("darrer caracter = lletra");
                }else {
                    if (EntrdaDatos.lletraDni(Integer.parseInt(nifTxt.getText().substring(0,8)))!=c){
                        jLabel1.setText("DNI incorrecte");
                    }
                }
            } else if (c < '0' || c > '9') {
                evt.consume();
                jLabel1.setText("nomes numeros");
            }
        }
    }//GEN-LAST:event_nifTxtKeyTyped


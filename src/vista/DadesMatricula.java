    public DadesMatricula(java.awt.Frame parent, boolean modal, String modo) {
        super(parent, modal);
        this.modo=modo;
        alumneJDBC = new AlumneJDBC();
        carnetJDBC = new CarnetJDBC();
        matriculaJDBC = new MatriculaJDBC();
        novaMatricula = new Matricula();
                
        totsAlumnes = alumneJDBC.seleccionarAllAlumnes();
        Alumne al = new Alumne();
        al.setCognoms("-- Selecciona alumne --");
        totsAlumnes.altaAlumne(al);
        Collections.sort(totsAlumnes.getLista());

        totsCarnets = carnetJDBC.totsCarnets();
        Carnet ca = new Carnet();
        ca.setTipus("-- Selecciona carnet --");
        totsCarnets.altaCarnet(ca);
        Collections.sort(totsCarnets.getLista());
        initComponents();
        formulario();
        if (modo.equals("modificar")) {                      
            this.setTitle("Modificar matricula");
        }else{            
            this.setTitle("Alta matricula");
        }    
    }
    private void formulario() {
        if (!modo.equals("modificar")) {            
            jComboBox1.setSelectedIndex(0);
            jComboBox2.setSelectedIndex(0);                    
        }
    }
    

    private boolean comprobarCampos() {
        if (jComboBox1.getSelectedIndex() < 1) {
            JOptionPane.showMessageDialog(this, "Has de seleccionar un alumne", "ERROR: Alumne incorrecte", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (jComboBox2.getSelectedIndex() < 1) {
            JOptionPane.showMessageDialog(this, "Has de seleccionar un carnet", "ERROR: Carnet incorrecte", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }
    private void aceptarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceptarBtnActionPerformed
        if (comprobarCampos()) {
            if (matriculaJDBC.insertarMatricula(novaMatricula)) {
                JOptionPane.showMessageDialog(this, "Matricula donada d'alta");
                
                dispose();

            } else {
                JOptionPane.showMessageDialog(this, "No s'ha pogut insertar la nova matricula", "ERROR. Matricula no donada d'alta", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_aceptarBtnActionPerformed
